<div id= "howToUseLabeler">
<h2>2. How to use the labeling tool?</h2>
<h2>2.1 How to insert a label?</h2>
<p>Move the green playing head to specify the beginning and the end of a label by clicking the corresponding buttons. Specify a label name. Click the "check" button.</p>
<h2>2.2 How to update a label?</h2>
<p>Choose a label in the list and the label will appear in the timeline. Specify a new beginning and end of the label. Specifiy a new label name. Click the "check" button.</p>
<h2>2.3 How to delete a label?</h2>
<p>Choose a label in the list and the label will appear in the timeline. Click the "cross" button.</p>
