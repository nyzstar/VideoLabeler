<h2>Some additional explanations on the behaviours:</h2>
<p>
drinking: defined by the mouse’s mouth being juxtaposed to the tip of the drinking spout<br/>
eating: defined by the mouse reaching and acquiring food from the food bin<br/>
grooming: defined by the forelimbs or hindlimbs sweeping across the face or torso, typically as the animal is reared up<br/>
hanging: defined by grasping of the wire bars with the forelimbs and/or hindlimbs, with at least two limbs off the ground<br/>
rearing: defined by an upright posture and forelimbs off the ground<br/>
resting: defined by inactivity or nearly complete stillness<br/>
walking: defined by ambulation<br/>
micromovements: defined by small movements of the animal’s head or limbs<br/>
</p>
